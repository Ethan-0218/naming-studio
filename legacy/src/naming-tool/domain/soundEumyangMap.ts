// 중성(모음) 인덱스 → 양: ㅏㅐㅑㅒㅗㅘㅙㅚㅛ, 음: 나머지
// 한글 음절 = 0xAC00 + (초성×21 + 중성)×28 + 종성
const YANG_JUNGSUNG = new Set([0, 1, 2, 3, 8, 9, 10, 11, 12]);

function soundEumyangFromVowel(hangul: string): Eumyang | null {
  if (!hangul) return null;
  const code = hangul.charCodeAt(0) - 0xac00;
  if (code < 0 || code > 11171) return null;
  const jungsungIndex = Math.floor((code % 588) / 28);
  return YANG_JUNGSUNG.has(jungsungIndex) ? '양' : '음';
}

export function soundEumyangFromHangul(hangul: string): Eumyang | null {
  return SOUND_EUMYANG_MAP[hangul] ?? soundEumyangFromVowel(hangul);
}
