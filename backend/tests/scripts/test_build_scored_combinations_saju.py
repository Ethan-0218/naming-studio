"""build_scored_combinations 사주보완(성+이름 3글자 자원오행) 점수 단위 테스트."""

from scripts.build_scored_combinations import _정규화_사주보완_점수


def test_세글자_모두_용신이면_1():
    assert _정규화_사주보완_점수("목", "목", "목", "목") == 1.0


def test_세글자_모두_기신이면_0():
    # 목의 기신은 금
    assert _정규화_사주보완_점수("목", "금", "금", "금") == 0.0


def test_자원오행_없으면_중립():
    assert _정규화_사주보완_점수("목", "", "", "") == 0.5


def test_성만_용신_한글자만_유효해도_극단값():
    assert _정규화_사주보완_점수("화", "화", "", "") == 1.0
    # 화의 기신은 수
    assert _정규화_사주보완_점수("화", "수", "", "") == 0.0
