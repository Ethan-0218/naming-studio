"""build_scored_combinations 사주보완(이름 2글자 자원오행) 점수 단위 테스트."""

from scripts.build_scored_combinations import _정규화_사주보완_점수


def test_두글자_모두_용신이면_1():
    # 목 용신, 이름 두 글자 모두 목 → 大吉 → 1.0
    assert _정규화_사주보완_점수("목", "목", "목") == 1.0


def test_용신_희신_각하나_대길_1():
    # 목 용신, 희신=수(수생목). 목+수 → net 2 → 大吉 → 1.0
    assert _정규화_사주보완_점수("목", "목", "수") == 1.0


def test_용신_하나_길_0_8():
    # 목 용신, h1=목 h2=토(기타) → net 1 → 吉 → 0.8
    assert _정규화_사주보완_점수("목", "목", "토") == 0.8


def test_기타만_평_0_5():
    # 목 용신, 두 글자 모두 토(기타) → 平 → 0.5
    assert _정규화_사주보완_점수("목", "토", "토") == 0.5


def test_자원오행_없으면_평_0_5():
    # 유효 오행 없음 → 平 → 0.5
    assert _정규화_사주보완_점수("목", "", "") == 0.5


def test_기신_하나_흉_0_3():
    # 목 용신, 기신=금. h1=금 h2=토 → net -1 → 凶 → 0.3
    assert _정규화_사주보완_점수("목", "금", "토") == 0.3


def test_두글자_모두_기신이면_0():
    # 목 용신, 기신=금. h1=금 h2=금 → 大凶 → 0.0
    assert _정규화_사주보완_점수("목", "금", "금") == 0.0


def test_화용신_두글자_모두_화_대길():
    # 화 용신, 두 글자 모두 화 → 大吉 → 1.0
    assert _정규화_사주보완_점수("화", "화", "화") == 1.0


def test_화용신_기신_수_대흉():
    # 화 용신, 기신=수. h1=수 h2=수 → 大凶 → 0.0
    assert _정규화_사주보완_점수("화", "수", "수") == 0.0
