# 발음오행: 한글 음절의 초성→오행 변환. domain/saju/오행.py 모델 재사용.

from domain.saju.오행 import 오행

_초성_오행: dict[str, 오행] = {
    "ㄱ": 오행.목, "ㅋ": 오행.목,
    "ㄴ": 오행.화, "ㄷ": 오행.화, "ㄹ": 오행.화, "ㅌ": 오행.화,
    "ㅇ": 오행.토, "ㅎ": 오행.토,
    "ㅅ": 오행.금, "ㅈ": 오행.금, "ㅊ": 오행.금,
    "ㅁ": 오행.수, "ㅂ": 오행.수, "ㅍ": 오행.수,
}

_초성_목록 = [
    "ㄱ", "ㄲ", "ㄴ", "ㄷ", "ㄸ", "ㄹ", "ㅁ", "ㅂ", "ㅃ",
    "ㅅ", "ㅆ", "ㅇ", "ㅈ", "ㅉ", "ㅊ", "ㅋ", "ㅌ", "ㅍ", "ㅎ",
]


def 발음오행_from_초성(char: str) -> 오행 | None:
    """한글 음절 1글자의 초성을 추출해 발음오행을 반환합니다. 한글이 아니거나 매핑 없으면 None."""
    if not char:
        return None
    code = ord(char) - 0xAC00
    if not (0 <= code <= 11171):
        return None
    초성 = _초성_목록[code // (21 * 28)]
    return _초성_오행.get(초성)
